# Data Types

# 1. String

print("Hello World")
print('Hello World')

# String Concatenation
print("Hello" + "World")
print("Hello" + " " + "World")

# [0] Represents 'H', [1] 'e' and so on...
print("Hello[0]")
print("Hello[3]")

# Can't sum using Strings
print("123" + "342")

# 2. Integer

print(123 + 342)

